package archivos;

/**
 *
 * @author LENOVO-PC
 */
public class ArchivoExtension {
    
    private String nombre,path, extension, tipo;

    public ArchivoExtension(String nombre, String path, String extension, String tipo) {
        this.nombre = nombre;
        this.path = path;
        this.extension = extension;
        this.tipo = tipo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getExtension() {
        return extension;
    }

    public void setExtension(String extension) {
        this.extension = extension;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
    
}
